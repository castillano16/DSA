// Create variables for first and last name
let favActorFirstName = "Lee";
let favActorLastName = "Min-ho";

// Concatenate first and last name
let fullName = favActorFirstName + " " + favActorLastName;

// Convert full name to uppercase
let uppercase = fullName.toUpperCase();

//  Create message with uppercase name
let message = `My favorite actor is ${uppercase}.`;

//  Append additional information
message += " His best show is The Legend of the Blue Sea.";

// Log variables to the console
console.log(favActorFirstName);
console.log(favActorLastName);
console.log(fullName);
console.log(uppercase);
console.log(message);